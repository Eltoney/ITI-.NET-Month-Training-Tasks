CREATE TABLE EMPLOYEE(
 SSN INT PRIMARY KEY , 
 BDate DATE , 
 HomeADDRESS VARCHAR(20),
 SEX VARCHAR(1), 
 SALARY INT,
 SUPERSSN INT FOREIGN KEY REFERENCES EMPLOYEE(SSN),
 FNAME VARCHAR(20),
 LNAME VARCHAR(20),
 MINIT INT
);

